public class DataTypes {
    public static void main(String[] args) {
        int a=4;
        char b='s';
        String c="siri";
        float d=10.5f;
        double e=10.3;
        Boolean f=true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
